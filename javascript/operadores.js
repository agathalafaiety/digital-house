//adição
console.log(2+2);

//subtração
console.log(10-5);

//multiplicação
console.log(10*5);

//divisão
console.log(10/2);

//incremento
let num = 10;
console.log(++num);

//decremento
console.log(--num);

//comparação maior
console.log(10 > 5);

//comparação menor
console.log(10 < 5);

//comparação menor ou igual
console.log(3 <= 3);

//comparação maior igual
console.log(20 >= 20);

//comparação de igualdade
console.log(10 == 10);

//omparação de igualdade rigida
console.log(10 === "10");

//comparação de diferente
console.log(10 != 10);

//comparação de diferente rigido
console.log(10 !== "10");

//comparador logico &&
console.log(10 > 5 && 8 < 10 && 11 > 5);

//comparador logico OR || 
console.log(10 > 5 || 8 < 10);

//comparador de negação !
console.log(!true);

//concatenação
let nome = "João";
let sobrenome = "Oliveira";

console.log(nome + " " + sobrenome);




